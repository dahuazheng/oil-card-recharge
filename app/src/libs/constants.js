// 常用数据验证规则
export const REG = {
    MOBILE: /^[0-9]{7,16}$/,
    EMAIL: /^[\w-.]+@([\w-]+\.)+[a-zA-Z]+$/,
    PASSWORD: /^(?!\d+$)(?![A-Za-z]+$)[a-zA-Z0-9]{8,16}$/, // 8-16 位英文和数字（英文数字都要有，忽略大小写）
    NUMBER: /^-?\d+(.\d+)?$/,
    ID_CARD: /^[a-z0-9A-Z]+$/,
    URL: /^https?:\/\//,
    IP: /^([0-9]{1,3})(\.[0-9]{1,3}){3}$/,
    SMS_CODE: /^[0-9]{4}$/,
}
